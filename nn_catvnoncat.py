def sigmoid(Z): # Sigmoid activation function
    
    A = 1/(1+np.exp(-Z))
    cache = Z
    
    return A, cache

def relu(Z): # RELU function.
    
    A = np.maximum(0,Z)
    
    assert(A.shape == Z.shape)
    
    cache = Z
    return A, cache


def relu_backward(dA, cache): # Back prop for a single RELU unit.
    
    Z = cache
    dZ = np.array(dA, copy=True) # just converting dz to a correct object.
    dZ[Z <= 0] = 0 # When z <= 0, set dz to 0 as well.
    
    assert (dZ.shape == Z.shape)
    
    return dZ

def sigmoid_backward(dA, cache): # Back prop for a single SIGMOID unit.
    
    Z = cache
    
    s = 1/(1+np.exp(-Z))
    dZ = dA * s * (1-s)
    
    assert (dZ.shape == Z.shape)
    
    return dZ


